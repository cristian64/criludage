    // funcion que nos lleva al panel anterior
    private void botonAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnteriorActionPerformed

        // si hay panel anterior
        if(panelActual>1){
            // decremento el contador
            panelActual--;
            // paso al panel anterior
            ((CardLayout)panelPrincipal.getLayout()).previous(panelPrincipal);
            // se puede ir hacia delante
            botonSiguiente.setEnabled(true);
        }
        // si estoy en el primer panel
        if(panelActual==1){
            // deshabilito el boton anterior
            botonAnterior.setEnabled(false);
        }
        
    }//GEN-LAST:event_botonAnteriorActionPerformed

    private void botonSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSiguienteActionPerformed

        // si no estoy en el ultimo panel
        if(panelActual < numPaneles){
            // incremento el contador
            panelActual++;
            // paso al siguiente panel
            ((CardLayout)panelPrincipal.getLayout()).next(panelPrincipal);
            // se puede ir al panel anterior
            botonAnterior.setEnabled(true);
        }
        // si estoy en el ultimo panel
        if(panelActual == numPaneles){
            // deshabilito el boton siguiente
            botonSiguiente.setEnabled(false);
        }
        
    }//GEN-LAST:event_botonSiguienteActionPerformed

