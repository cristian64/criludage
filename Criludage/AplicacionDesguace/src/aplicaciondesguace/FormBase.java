    // anyade un panel a la navegacion
    public void addPanel(JPanel p){
        
        // borro todos los paneles que hay despues del actual
        for(int i=panelActual;i<paneles.size();i++){
            panelPrincipal.remove(i);
            paneles.remove(i);
            //System.out.println("borro");
            i--;
        }
        // actualizo el numero de paneles
        numPaneles=panelActual;

        // anyado el nuevo panel como ultimo
        numPaneles++;
        panelActual=numPaneles;
        panelPrincipal.add(p,""+numPaneles);
        ((CardLayout)panelPrincipal.getLayout()).show(panelPrincipal, ""+numPaneles);
        panelPrincipal.updateUI();
        botonAnterior.setEnabled(true);
        botonSiguiente.setEnabled(false);
        paneles.add(p);
    }

    // borra el ultimo panel
    public void deletePanel(){

        // me voy al anterior
        ((CardLayout)panelPrincipal.getLayout()).previous(panelPrincipal);
        // borro el ultimo
        panelPrincipal.remove(numPaneles-1);
        paneles.remove(numPaneles-1);

        // actualizo las variables
        numPaneles--;
        panelActual=numPaneles;

        // actualizo
        panelPrincipal.updateUI();
        // activo o desactivo el boton anterior
        if(numPaneles>1){
            botonAnterior.setEnabled(true);
        }else{
            botonAnterior.setEnabled(false);
        }
        botonSiguiente.setEnabled(false);

    }

    // escribe el mensaje en el fichero de log
    private void addLog(String s){

        // creo las variables
        FileWriter fichero = null;
        PrintWriter pw = null;
        try
        {
            // abro el fichero
            // el segundo parametro es para que todo se anyada al final del fichero
            fichero = new FileWriter("Registro.log",true);
            pw = new PrintWriter(fichero);
            // escribo la fecha y hora y el mensaje
            pw.println(ImprimirFecha(new GregorianCalendar()) + " " + s);

        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
           try {
           if (null != fichero)
              fichero.close();
           } catch (Exception e2) {
              e2.printStackTrace();
           }
        }
    }

    // imprime una fecha formateada
    public String ImprimirFecha(GregorianCalendar f) {

        DecimalFormat format = new DecimalFormat("00");

        return format.format(f.get(GregorianCalendar.DAY_OF_MONTH)) + "." +
                format.format(f.get(GregorianCalendar.MONTH)+1) + "." +
                format.format(f.get(GregorianCalendar.YEAR)) + " " +
                format.format(f.get(GregorianCalendar.HOUR_OF_DAY)) + ":" +
                format.format(f.get(GregorianCalendar.MINUTE)) + ":" +
                format.format(f.get(GregorianCalendar.SECOND));
    }

